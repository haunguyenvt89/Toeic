public class LoaiCH {
	private int Maloai;
	private String TenLoai;
	private int Phanthi;
	public int getMaloai() {
		return Maloai;
	}
	public void setMaloai(int maloai) {
		Maloai = maloai;
	}
	public String getTenLoai() {
		return TenLoai;
	}
	public void setTenLoai(String tenLoai) {
		TenLoai = tenLoai;
	}
	public int getPhanthi() {
		return Phanthi;
	}
	public void setPhanthi(int phanthi) {
		Phanthi = phanthi;
	}
	
}
