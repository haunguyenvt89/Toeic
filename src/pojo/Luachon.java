package pojo;

public class Luachon {
	
	private String MaLC;
	private String MaCH;
	private String Noidung_Text;
	private String Noidung_MP3;
	private boolean Ladapan;
	public String getMaLC() {
		return MaLC;
	}
	public void setMaLC(String maLC) {
		MaLC = maLC;
	}
	public String getMaCH() {
		return MaCH;
	}
	public void setMaCH(String maCH) {
		MaCH = maCH;
	}
	public String getNoidung_Text() {
		return Noidung_Text;
	}
	public void setNoidung_Text(String noidung_Text) {
		Noidung_Text = noidung_Text;
	}
	public String getNoidung_MP3() {
		return Noidung_MP3;
	}
	public void setNoidung_MP3(String noidung_MP3) {
		Noidung_MP3 = noidung_MP3;
	}
	public boolean isLadapan() {
		return Ladapan;
	}
	public void setLadapan(boolean ladapan) {
		Ladapan = ladapan;
	}
	
}
