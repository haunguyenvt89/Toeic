package pojo;

import java.sql.Date;

public class Dethi {
	
	private String Made;
	private Date Ngaytao;
	private int ThoigianLB;
	String getMade() {
		return Made;
	}
	void setMade(String made) {
		Made = made;
	}
	Date getNgaytao() {
		return Ngaytao;
	}
	void setNgaytao(Date ngaytao) {
		Ngaytao = ngaytao;
	}
	int getThoigianLB() {
		return ThoigianLB;
	}
	void setThoigianLB(int thoigianLB) {
		ThoigianLB = thoigianLB;
	}

}
